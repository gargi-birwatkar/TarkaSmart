CREATE OR REPLACE FUNCTION "public"."match_document_chunks"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer, "user_id_filter" "text") RETURNS TABLE("id" "uuid", "content" "text", "source_name" "text", "page_number" integer, "similarity" double precision)
    LANGUAGE "sql"
    AS $$
  SELECT
    dc.id,
    dc.content,
    dc.source_name,
    dc.pg_no,
    1 - (dc.embedding <=> query_embedding) AS similarity
  FROM document_chunks dc
  JOIN documents d ON dc.document_id = d.id
  WHERE 1 - (dc.embedding <=> query_embedding) > match_threshold
    AND d.google_user_id = user_id_filter
  ORDER BY dc.embedding <=> query_embedding
  LIMIT match_count;
$$;


